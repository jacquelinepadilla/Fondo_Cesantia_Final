     private void cargar_lista_de_clientes(){
        try{
            Connecion con = new Connecion();
            cliente_servicios Cliente_servicio = new cliente_servicios();
            cnx = con.obtener(); 
            if (cnx!=null)
            {
                this.Cliente = this.servi.recuperarTodas(Connecion.obtener());
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                for(int i = 0; i < this.Cliente.size(); i++){
                dtm.addRow(new Object[]{
                    this.Cliente.get(i).getId_Cliente(),
                    this.Cliente.get(i).getApellidos(),
                    this.Cliente.get(i).getNombres(),
                    this.Cliente.get(i).getCedula(),
                    this.Cliente.get(i).getFecha_nacimiento(),
                    this.Cliente.get(i).getDireccion()     
                });
            }
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }

